#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*result;
	size_t	i;
	size_t	len;

	len = ft_strlen(s);
	result = (char *)malloc(len + 1);
	if (!result)
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
